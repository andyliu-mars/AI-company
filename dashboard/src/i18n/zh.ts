type DeepLoosen<T> = {
  [K in keyof T]: T[K] extends (...args: infer A) => infer R
    ? (...args: A) => R
    : T[K] extends string
      ? string
      : DeepLoosen<T[K]>;
};

export type Translations = DeepLoosen<typeof zh>;
